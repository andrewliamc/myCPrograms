// Practice printing statements using the printf function
// all statements must go inside the main function

#include <stdio.h>

int main(void)
{
  printf("Hello World\n");
  // printf is the function and "Hello World" is an argument, and we are passing the argument into the function
  // "Hello World" is a string, which is just the combination of characters (known as char)
  printf("Line 2\n");
  printf("Lines 3\n");
  return 0;
}